var a = 10;
var b = 10;
// Interpolated strings begin and end with backticks (`)
// It is the same as using string concatenation ("string1" + "string2")
console.log(`The sum of ${a} and ${b} is ${a+b}!`);
// Overall, it is less punctuation to type compared to plain 
console.log("The sum of "+a+" and " + b + " is " + (a+b) + "!");